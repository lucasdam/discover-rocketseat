/* 7 - Operadores Matemáticos */

/* +, adição */
select * from aluno
where matricula = 2 + 1

/* -, subtração */
select * from aluno
where matricula = 4 - 3

/* *, multiplicação */
select * from aluno
where matricula = 3 * 1

/* /, divisão */
select * from aluno
where matricula = 4 / 2

/* %, módulo */
select * from aluno
where matricula = 3 % 2