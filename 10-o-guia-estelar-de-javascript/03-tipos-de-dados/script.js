// String, Template Literals
console.log('Lucas')
console.log('"Lucas"')
console.log(`Lucas tem ${80} anos`)

// Number
console.log(33)
console.log(12.5 + 5)
console.log(7.5 / "nfaif")
console.log(Infinity)

// Boolean
console.log(true)
console.log(false)

// Undefined vs null
console.log(undefined) // indefinido, não existe
console.log(null) // objeto que não tem nada dentro

// Object
console.log({
    name: "Lucas",
    idade: 80,
    andar: function(){
        console.log('andar')
    }
})

// Array
console.log(["Leite", "Pão", 2, 3, true])